<h1 style="color:#e93657;" class="titc">Praline d' autore</h1>
<br />
<p class="testoc">
  In questi giorni di Lock Down &eacute; maturata un'idea. Perch&eacute; non
  provare a inventarsi qualcosa di nuovo e di particolare? Ecco la scintilla
  delle "Praline d'autore". Ci &eacute; subito piaciuta l'idea di legare il
  gusto di un cioccolatino ad un opera letteraria, e da li &eacute; venuta da
  s&eacute; l' intuizione di avvalersi della tecnologia per rendere ancora
  pi&uacute; immersiva questa esperienza: un QRcode per richiamare un brano
  musicale che potesse accompagnare la lettura e la degustazione. Ed eccoci qui,
  pronti per lo speciale SAN VALENTINO. 4 coppie di cuori di cioccolato ripieni
  da morbide ganache al caramello, al cocco, aftereight e alla grappa. Il tutto
  rigorosamente da gustare assieme, leggendo una poesia con un brano musicale di
  sottofondo. Un modo inusuale e originale per dirsi Ti amo!
</p>
